    val small: androidx.compose.ui.unit.Dp = 4.dp,
    val medium: androidx.compose.ui.unit.Dp = 8.dp,
    val large: androidx.compose.ui.unit.Dp = 16.dp,
    val extraLarge: androidx.compose.ui.unit.Dp = 32.dp
)
